	<h2> Resep Nasi Goreng Sederhana</h2>

	<p>	Bahan :
		<ul>
		<li>500 gram nasi putih</li>
		<li> Butir Telur</li>
		<li>3 – 4 sendok makan kecap manis</li>
		<li>6 sendok makan minyak untuk menumis</li>
		<li>1 – 2 sendok makan saos tomat</li>
		</ul>
<br>
		Bumbu :
		<ul>
		<li>3 siung bawang putih</li>
		<li>6 siung bawang merah</li>
		<li>3 – 5 buah cabai merah atau sesuai selera</li>
		<li>1/2 sendok teh terasi</li>
		<li>Garam secukupnya</li>
		</ul>
<br><br>
		Cara Membuat :<br>


		<ol> 
		<li>Haluskan semua bumbu nasi goreng terlebih dahulu, setelah itu masukkan minyak ke dalam wajan, kemudian masukkan 1 butir telur lalu orak-arik hingga matang. Angkat dan dinginkan.</li>
		<li>Tumis bumbu yang telah dihaluskan hingga berbau harum.</li>
		<li>Masukkan nasi putih, lalu aduk-aduk hingga rata.</li>
		<li>Setelah rata, masukkan saos tomat, kecap manis dan telur, kemudian aduk hingga rata.</li>
		<li>Angkat dan nasi goreng siap untuk dihidangkan.</li>
		</ol>
	</p>	

